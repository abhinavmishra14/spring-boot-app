public class UserV1 {
	
	/** The name. */
	private String name;

	/**
	 * Instantiates a new user V 1.
	 */
	public UserV1() {
		super();
	}

	/**
	 * Instantiates a new user V 1.
	 *
	 * @param name the name
	 */
	public UserV1(final String name) {
		super();
		this.name = name;
	}

	/**
	 * Gets the name.
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * Sets the name.
	 *
	 * @param name the new name
	 */
	public void setName(final String name) {
		this.name = name;
	}
}
